public class MusicPlayer {
    public void on (){
        System.out.println("Musicplayer is On");
    }
    public void cd (){
        System.out.println("Musicplayer is set for CD");
    }
    public void radio (){
        System.out.println("Musicplayer is set for Radio");
    }
    public void off (){
        System.out.println("Musicplayer is OFF");
    }
}
